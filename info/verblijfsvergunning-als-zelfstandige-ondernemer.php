  <h2>
  	Zelfstandige kunstenaars</h2>

  <p>
  Turkse burgers vallen onder het associatierecht en worden vrijgesteld van het MVV-vereiste en het puntensysteem.
  In andere EU-landen kunt u de status <strong>EU langdurig ingezetene</strong> krijgen. Dit is een Europese variant van de permanente verblijfsvergunning. Als u met dergelijke Europese vergunning voor langdurig ingezetenen vanuit een andere Europees land in Nederland wilt ondernemen, gelden versoepelde voorwaarden.
  Onder bepaalde voorwaarden kunnen zelfstandige ondernemers uit de VS en Japan op basis van het Nederlands-Amerikaans vriendschapsverdrag (453) of het Nederlands-Japans Handelsverdrag (510) een verblijfsvergunning zelfstandige krijgen zonder dat zij hoeven aan te tonen dat hun onderneming een economische meerwaarde heeft voor Nederland.
  Zelfstandige kunstenaars van buiten de EU moeten aantonen dat hun werk cultureel belang heeft voor Nederland om in aanmerking te komen voor een verblijfsvergunning.</p>

