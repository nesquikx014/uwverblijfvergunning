  <h2>Verblijf bij (ongehuwde) partner</h2>
  <p>
  Wilt u uw echtgenoot, partner of kinderen naar Nederland laten komen om bij u in Nederland te komen wonen? Dan dient u een verblijfsaanvraag aan te vragen. Deze procedure staat bekend als de MVV/TEV-procedure. De eenvoudigste manier is om dit vanuit Nederland te doen. In dat geval wordt u door de IND aangeduid als de referent.</p>

  <p>Met een MVV-sticker in het paspoort heeft de vreemdeling toestemming om naar Nederland te komen. Sinds de nieuwe procedure voor ‘Toelating en Verblijf’ wordt na binnenkomst in Nederland met een MVV automatisch een verblijfsvergunning afgegeven.</p>

  <p>De IND vereist dat u beschikt over voldoende duurzame middelen van bestaan, met een inkomen dat minimaal gelijk is aan het wettelijk minimumloon op basis van een jaarcontract. Er zijn uitzonderingen op deze inkomenseis mogelijk.</p>

  <p>Bij het indienen van een MVV-aanvraag moeten veel formulieren worden ingevuld en moeten stukken uit het buitenland worden gelegaliseerd of voorzien zijn van een apostille. Als een document is opgesteld in een andere taal dan het Nederlands, Engels, Frans of Duits, moet het eerst worden vertaald.</p>

  <p>Voor het indienen van een MVV-aanvraag bestaat geen recht op gefinancierde rechtsbijstand. De Raad voor Rechtsbijstand verleent geen toevoeging voor het indienen van een MVV-aanvraag, zelfs niet na doorverwijzing door het Juridisch Loket.</p>

  <p>Is uw echtgenoot of partner al in Nederland? Dan geeft de IND aan dat hij of zij in veel gevallen toch weer terug moet naar het land van herkomst om alsnog een MVV aan te vragen. Het bezit van een MVV is namelijk een voorwaarde voor een verblijfsvergunning, en het ontbreken ervan wordt door de IND vaak gebruikt om een aanvraag voor een verblijfsvergunning af te wijzen. Er zijn echter uitzonderingen op deze regel!</p>

