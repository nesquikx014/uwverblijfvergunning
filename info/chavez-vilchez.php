  <h2>Chavez-Vilchez</h2>
  <h2>
  	Verblijf bij minderjarige Nederlands kind (Chavez-Vilchez)</h2>

  <p>
  Op 10 mei 2017 heeft het Hof van Justitie in het arrest (uitspraak) Chavez-Vilchez vastgesteld dat een ouder die niet de nationaliteit heeft van een EU-land (een derdelander) die ouder is van een minderjarig Nederlands kind onder voorwaarden aanspraak kan maken op een verblijfsrecht in de Europese Unie (EU).</p>

  <p>In het Chavez-Vilchez-arrest werd het belang van het kind vooropgesteld: het kind heeft recht op omgang met beide ouders. Als aan de ‘derdeland-ouder’ geen verblijfsrecht wordt toegekend, komt het erop neer dat het kind wordt gedwongen het grondgebied van de Unie te verlaten. Als er een sterke afhankelijkheidsrelatie is tussen de derdelander en het Nederlandse kind, heeft de derdelander recht op een afgeleid verblijfsrecht.</p>

  <h2>
  	De regels in het kort:</h2>

  <p> De vreemdeling moet zijn identiteit en nationaliteit aannemelijk maken door het overleggen van een geldig document voor grensoverschrijding of een geldige identiteitskaart. Als de vreemdeling hieraan niet kan voldoen, moet hij zijn identiteit en nationaliteit ondubbelzinnig aantonen met andere middelen;</p>

  <p> De vreemdeling heeft een minderjarig kind (dat wil zeggen: beneden de achttien jaar) dat in het bezit is van de Nederlandse nationaliteit;</p>

  <p> De vreemdeling verricht al dan niet gezamenlijk met de andere ouder daadwerkelijke zorgtaken ten behoeve van het minderjarige kind;</p>

  <p> Tussen de vreemdeling en het kind bestaat een zodanige afhankelijkheidsverhouding dat het kind gedwongen zou zijn het grondgebied van de Unie te verlaten als aan de vreemdeling een verblijfsrecht wordt geweigerd.</p>

  <p>De IND heeft de regels in juni 2018 aangescherpt. Door de IND worden nu zeer hoge eisen gehanteerd ten aanzien van de zorg- en opvoedingstaken van de derdelander ouder.
  Alleen een ouder van een Nederlands kind komt in aanmerking voor een verblijfsrecht in Nederland. Het kind van een Nederlander krijgt bij geboorte automatisch de Nederlandse nationaliteit.</p>

  <p>De niet-EU ouder van een Nederlands kind heeft alleen een verblijfsrecht in Nederland zo lang het kind minderjarig is. Zijn er meerdere kinderen met de Nederlandse nationaliteit, dan geldt het verblijfsrecht in ieder geval tot het jongste kind 18 jaar wordt.</p>

  <p>De derdelander ouder moet (mede) verantwoordelijk zijn voor de zorg en opvoeding van het Nederlandse kind om in aanmerking te komen voor een verblijfsrecht op grond van het arrest Chavez-Vlichez.</p>

  <p>De IND stelt zeer hoge eisen aan het door de ouder te leveren bewijs van de zorg- en opvoedingstaken van zijn of haar Nederlandse kind. De kans op een succesvolle aanvraag neemt toe als veel stukken bij de aanvraag worden overgelegd en de persoonlijke situatie goed</p>

  <p>wordt uitgelegd. Hierbij kan worden gedacht aan een omgangsregeling, ouderschapsplan, foto’s (met datum waarop de foto’s genomen zijn), verklaringen van school of kinderdagverblijf, verklaringen van verloskundige en/of consultatiebureau, de huisarts, etc.</p>

