<section class="article-section">
  <h2>Bezwaar en beroep tegen IND-beslissingen</h2>
  <p>
    Een afwijzing of intrekking van uw verblijfsvergunning is ingrijpend. Binnen korte termijnen moet een goed onderbouwd
    <strong>bezwaarschrift</strong> of <strong>beroepschrift</strong> worden ingediend. Wij analyseren de motivering en bouwen een stevige tegenargumentatie op met
    jurisprudentie en aanvullend bewijs.
  </p>
</section>

<section class="article-section">
  <h2>Wilt u bezwaar maken?</h2>
  <ul class="article-list">
    <li>Termijn: binnen 4 weken na dagtekening van de beslissing.</li>
    <li>Vereisten: gemotiveerde gronden, ondertekend en voorzien van bewijsstukken.</li>
    <li>Resultaat: heroverweging door dezelfde IND-dienst, vaak inclusief hoorzitting.</li>
  </ul>
</section>

<section class="article-section">
  <h2>Naar de rechtbank in beroep</h2>
  <p>
    Als het bezwaar wordt afgewezen, kunt u binnen 4 weken beroep instellen bij de rechtbank. De rechter beoordeelt of de IND een zorgvuldige
    procedure heeft gevolgd en de wet juist heeft toegepast. In sommige gevallen vragen we een <strong>voorlopige voorziening</strong> aan om uitzetting te voorkomen.
  </p>
</section>

<section class="article-section">
  <h2>Onze juridische aanpak</h2>
  <div class="article-grid">
    <div>
      <h3>Dossieranalyse</h3>
      <p>We toetsen de motivering van de IND aan beleid, wetgeving en recente jurisprudentie.</p>
    </div>
    <div>
      <h3>Bewijsverzameling</h3>
      <p>We vragen aanvullende verklaringen op bij werkgevers, artsen of scholen en reconstrueren het feitencomplex.</p>
    </div>
    <div>
      <h3>Procesvertegenwoordiging</h3>
      <p>Wij vertegenwoordigen u tijdens de hoorzitting en bij de rechtbank. U ontvangt een duidelijke strategie en conceptvragen.</p>
    </div>
  </div>
</section>

<section class="article-section">
  <h2>Checklist voor een sterke zaak</h2>
  <ul class="article-list article-list--success">
    <li>Lees de beslissing grondig en markeer onduidelijkheden of feitelijke onjuistheden.</li>
    <li>Bewaar alle correspondentie en ontvangstbevestigingen.</li>
    <li>Verzamel direct nieuwe bewijsstukken; hoe verser, hoe sterker.</li>
    <li>Noteer belangrijke data en termijnen in uw agenda.</li>
  </ul>
</section>

<section class="article-section">
  <h2>Tijdlijn bezwaarprocedure</h2>
  <ol class="article-stepper">
    <li>
