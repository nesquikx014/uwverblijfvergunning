  <h2>Verblijfsvergunning medische gronden</h2>
  <p>
  U kunt als vreemdeling ook een verblijfsvergunning medische gronden aanvragen. Als u al in Nederland verblijft en een medische behandeling ondergaat die u niet in uw land van herkomst kunt krijgen en het stopzetten van de behandeling zal resulteren in een medisch noodsituatie kunt u een verblijfsvergunning medische behandeling aanvragen.</p>

  <h2>
  	Verblijfsvergunning medische gronden vanuit buitenland</h2>

  <h3>Hulp nodig bij allerlei juridische kwesties? Contacteer ons nu.</h3>

  <p>De Immigratie- en Naturalisatiedienst (IND) laat zich zich adviseren het Bureau Medische Advisering (BMA). Deze beoordeeld of er sprake is van medische noodsituatie en of een Verblijfsvergunning medische gronden wordt toegekend. Het protocol van het BMA vindt u op de site van de IND (pdf)
  Vreemdelingen die nog niet in Nederland verblijven of waarvan de medische behandeling nog niet is begonnen kunnen ook verblijf voor een medische behandeling aanvragen. In dit geval dient Nederland internationaal gezien als de expert voor de medisch noodzakelijke behandeling van de betreffende aandoening. In dit geval is het ook belangrijk dat u de financiering van de behandeling geregeld heeft en moeten u kunnen aantonen over voldoende eigen middelen beschikken om in uw levensonderhoud te voorzien.
