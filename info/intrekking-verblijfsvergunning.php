  <h2>
  	Bezwaarmogelijkheden</h2>

  <p>
  Wanneer uw verblijfsvergunning wordt ingetrokken, heeft dat grote gevolgen. U moet het land verlaten en als u dat niet doet, bent u illegaal in Nederland. De overheid kan u dan arresteren, vastzetten en gedwongen uitzetten. Gelukkig kunt u zich tegen de intrekking van uw verblijfsvergunning verweren met een zienswijze of bezwaarschrift.
  Een verblijfsvergunning kan om verschillende redenen worden ingetrokken. Bijvoorbeeld omdat u niet langer aan de inkomenseisen voldoet of omdat u niet meer getrouwd bent met degene bij wie u verblijft. Maar ook als u buiten Nederland bent gaan wonen, een misdrijf heeft gepleegd of wanneer u als student niet genoeg studiepunten heeft gehaald, kan uw verblijfsvergunning worden ingetrokken.
  In al deze gevallen kunt u bezwaar maken of in (hoger)beroep gaan. Maar dat moet dan wel binnen vier weken gebeuren nadat u heeft dat de verblijfsvergunning wordt ingetrokken of dat een eerder bezwaar is afgewezen. Is het besluit langer geleden kan u niet meer in bezwaar of beroep gaan.</p>

