<section class="article-section">
  <h2>Veelvoorkomende vragen</h2>
  <div class="article-callout">
    <h3>Kan ik mijn oorspronkelijke nationaliteit behouden?</h3>
    <p>Dat hangt af van uw land. Er bestaan uitzonderingen, bijvoorbeeld voor vluchtelingen en gehuwden met een Nederlander.</p>
  </div>
  <div class="article-callout">
    <h3>Wat als ik schulden of een boete heb?</h3>
    <p>Sommige boetes kunnen leiden tot afwijzing. Wij beoordelen de risico's en adviseren over het juiste moment van aanvragen.</p>
  </div>
  <div class="article-callout">
    <h3>Ik ben gehuwd met een Nederlander. Geldt de vijfjaarstermijn?</h3>
    <p>In veel gevallen geldt een verkorte termijn van drie jaar mits u samenwoont en het huwelijk bestendig is.</p>
  </div>
</section>

