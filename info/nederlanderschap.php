<section class="article-section">
  <h2>Nederlanderschap aanvragen</h2>
  <p>
    Naturalistatie geeft u stemrecht, een Nederlands paspoort en toegang tot alle sociale voorzieningen. De procedure verloopt via de gemeente
    waar u woont en mondt uit in een Koninklijk Besluit. Vanaf 2024 gelden strengere eisen voor taalniveau en integratie.
  </p>
</section>

<section class="article-section">
  <h2>Voorwaarden op hoofdlijnen</h2>
  <ul class="article-list">
    <li>Minimaal vijf jaar onafgebroken legaal verblijf (korter bij huwelijk of erkende vergunningen).</li>
    <li>Slagen voor het inburgeringsexamen op niveau A2 (met uitzonderingen en vrijstellingen).</li>
    <li>Geen zware strafrechtelijke antecedenten binnen de laatste vier jaar.</li>
    <li>Bereidheid om (eventueel) afstand te doen van uw huidige nationaliteit, tenzij een uitzondering van toepassing is.</li>
  </ul>
</section>

