<section class="article-section">
  <h2>Gezinshereniging in Nederland: de basis</h2>
  <p>
    Gezinshereniging is mogelijk voor partners, echtgenoten, kinderen en in specifieke gevallen ouders. De kernvoorwaarden zijn een
    duurzame relatie, voldoende inkomen van de referent en een positieve antecedentencheck. Afhankelijk van het land van herkomst is een
    <strong>Machtiging tot Voorlopig Verblijf (MVV)</strong> vereist.
  </p>
</section>

<section class="article-section">
  <h2>Wie komt in aanmerking?</h2>
  <ul class="article-list">
    <li><strong>Partners en echtgenoten:</strong> minimaal 21 jaar en een duurzame, exclusieve relatie aantonen.</li>
    <li><strong>Minderjarige kinderen:</strong> onder de 18 jaar en deel uitmaken van het huishouden van de referent.</li>
    <li><strong>Gewezen partners met gezamenlijk gezag:</strong> onder voorwaarden als er sprake is van een afhankelijk kind.</li>
    <li><strong>Ouders van alleenstaande minderjarige vreemdelingen:</strong> bij aantoonbaar ouderlijk gezag.</li>
  </ul>
</section>

