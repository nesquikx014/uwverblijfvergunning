  <h2>Studeren in Nederland</h2>
  <p>
  Ben je student afkomstig uit een land buiten de EU en wil je een opleiding in Nederland volgen? Dan heb je een verblijfsvergunning nodig.</p>

  <h2>
  	Werken tijdens je studie</h2>

  <p>De universiteit of hogeschool vraagt de verblijfsvergunning aan, je moet alleen wel aan bepaalde voorwaarden voldoen. Ook tijdens je studie heb je verplichtingen.
  Als student afkomstig uit een land buiten de EU mag je tijdens je studie in Nederland 16 uur per week werken. In plaats daarvan mag je ook seizoenswerk doen in juni, juli en augustus. In beide gevallen heb je een werkvergunning nodig.</p>

  <p>Om stage te lopen tijdens je studie in Nederland heb je geen <a href="https://www.everaert.nl/nl/werkvergunning">werkvergunning</a> nodig. Wel moet de stage relevant zijn voor de studie en moet het tijdelijke dienstverband worden vastgelegd in een stageovereenkomst.</p>

  <h2>
  	Werken na je studie (zoekjaar)</h2>

  <p>Bovendien mogen studenten ook ondernemingsactiviteiten verrichten tijdens hun studie. Hiervoor hebben zij geen werkvergunning nodig, zolang de studie centraal blijft staan en de prestaties niet lijden onder de ondernemingsactiviteiten.
  Ben je een buitenlandse student in Nederland en wil je na je studie in Nederland werken? Er zijn verschillende mogelijkheden.</p>

  <p>Als pas afgestudeerde kun je een verblijfsvergunning voor een zoekjaar aanvragen. Die is een jaar geldig en je hebt geen <a href="https://www.everaert.nl/nl/werkvergunning">werkvergunning</a> nodig om te werken in Nederland. Het is een ideale manier om je te oriënteren op de Nederlandse arbeidsmarkt en uiteindelijk een baan te vinden.</p>

  <p>Misschien heb je al werk gevonden en wil je na je studie direct aan de slag. Je verblijfsvergunning als student is vanaf het moment dat je afstudeert niet meer geldig. Het is belangrijk dat je een andere verblijfsvergunning aanvraagt voordat dit moment aanbreekt.</p>

  <p>Er zijn verschillende verblijfsvergunningen om te werken in Nederland.</p>

  <p>Voor sommige categorieën gelden speciale regels:</p>

