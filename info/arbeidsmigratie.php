<section class="article-section">
  <h2>Arbeidsmigratie en ondernemersvisa</h2>
  <p>
    Nederland biedt diverse verblijfsroutes voor talentvolle professionals en ondernemers. Voor werkgevers is het van belang om
    te bepalen of de werknemer onder de regeling <strong>kennismigrant</strong>, <strong>EU intra-company transfer</strong> of een andere categorie valt.
    Ondernemers kunnen een beroep doen op de <strong>start-up</strong>, <strong>zelfstandige</strong> of <strong>zelfstandige met gecombineerde vergunning (GVVA)</strong>.
  </p>
</section>

<section class="article-section">
  <h2>Kennismigranten en werkgevers</h2>
  <div class="article-grid">
    <div>
      <h3>Voorwaarden werkgever</h3>
      <ul class="article-list">
        <li>De organisatie is erkend referent bij de IND of wij begeleiden het erkenningstraject.</li>
        <li>Voor iedere kennismigrant wordt het salaris correct vastgesteld en maandelijks uitbetaald.</li>
        <li>Het contract bevat een volledige rolomschrijving en looptijd.</li>
      </ul>
    </div>
    <div>
      <h3>Salarisnormen 2024</h3>
      <ul class="article-list">
        <li>Vanaf 30 jaar: € 5.331 bruto per maand (excl. vakantiegeld)</li>
        <li>Jonger dan 30 jaar: € 3.909 bruto per maand</li>
        <li>Zuivere starters (na studie): € 2.801 bruto per maand</li>
      </ul>
      <p class="article-note">Indexatie per 1 januari 2024. Check voor indiening altijd de meest recente bedragen.</p>
    </div>
    <div>
      <h3>Onze rol</h3>
      <ul class="article-list">
        <li>Opstellen en review van arbeidsovereenkomsten en salarisberekeningen</li>
        <li>Indienen verblijfsaanvragen via het zakelijke portaal van de IND</li>
        <li>Workshop voor HR over meldplicht en bewaarplicht</li>
      </ul>
    </div>
  </div>
</section>

<section class="article-section">
  <h2>Ondernemers en zelfstandigen</h2>
  <ul class="article-list">
    <li><strong>Zelfstandige zonder personeel (ZZP):</strong> Vereist een ondernemingsplan met economische meerwaarde, financiële prognoses en bewijs van opdrachten.</li>
    <li><strong>Start-up:</strong> In samenwerking met een erkende facilitator, inclusief een innovatieve propositie en 30% eigen middelen.</li>
    <li><strong>Investeerder:</strong> Mogelijk vanaf een investering van € 1.250.000 in een innovatieve onderneming of fonds.</li>
  </ul>
  <div class="article-callout article-callout--tip">
    <strong>Tip:</strong> Wij werken samen met fiscalisten en boekhouders om de financiële onderbouwing volgens IND-standaarden te leveren.
  </div>
</section>

