<section class="article-section">
  <h2>Compliance en meldplicht</h2>
  <p>
    Erkende referenten hebben een meldplicht voor wijzigingen binnen 28 dagen. Denk aan beëindiging van het dienstverband, salariswijzigingen
    of uitblijven van betaling. Wij bieden abonnementen voor dossierbewaking en periodieke audits om boetes te voorkomen.
  </p>
</section>

