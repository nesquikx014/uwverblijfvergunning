  <h2>Nederlander worden</h2>
  <h2>
  	Nederlander worden kan op 3 manieren:</h2>

  <p> Naturalisatie;</p>

  <p> Een optieprocedure;</p>

  <p> Van rechtswege.</p>

  <h2>
  	Naturalisatie</h2>

  <p>
  In de naturalisatieprocedure dient u een verzoek in om Nederlander te worden. Dit verzoek dient u in bij de gemeente. De gemeente bekijkt globaal of u aan de voorwaarden voldoet en geeft soms advies om de aanvraag niet, of nog niet in te dienen. Het is de IND die een beslissing neemt op het verzoek om naturalisatie. De IND mag één jaar doen over het nemen van een beslissing op het verzoek om Nederlander te worden.</p>

  <p>Na vijf jaar aaneengesloten rechtmatig verblijf in Nederland kunt u een verzoek om naturalisatie tot Nederlander indienen. Voor vreemdelingen die met een Nederlander zijn gehuwd of als geregistreerd partner hebben samengewoond, geldt in sommige gevallen een termijn van drie jaar.</p>

  <p>Hoofdregel is dat u geslaagd moet zijn voor een inburgeringsexamen. Bovendien moet u als u Nederlander wil worden afstand doen van uw oorspronkelijke nationaliteit, tenzij de nationaliteitwetgeving van uw eigen land dat niet toestaat. Hier gelden echter uitzonderingen!</p>

  <p>Bent u drie jaar gehuwd met een persoon die op het moment van de aanvraag voor naturalisatie Nederlander is dan hoeft u ook geen afstand te doen van de Nederlandse nationaliteit.</p>

  <h2>
  	De optieprocedure</h2>

  <p>De meest voorkomende reden die de IND heeft om een verzoek tot naturalisatie af te wijzen zijn openbare ordeproblemen.
  De optieprocedure is een eenvoudige manier om Nederlander te worden. Slechts in een beperkt aantal gevallen is verkrijging van het Nederlanderschap door optie mogelijk.</p>

  <p><strong>In nederland geboren en meerderjarig of sinds uw 4e in Nederland en meerderjarig</strong></p>

  <p>Bent u in Nederland, Aruba, Curaçao, Sint-Maarten of de openbare lichamen Bonaire, Sint Eustatius en Saba geboren en heeft u daar sinds uw geboorte gewoond of woont u hier sinds uw vierde? Dan is een optie een mogelijkheid.</p>

  <p><strong>Niet biologische ouder en kind erkend?</strong></p>

  <h2>
  	Van rechtswege</h2>

  <p>Indien uw het kind, waarvan u niet de biologische ouder bent, heeft erkend én het kind gedurende drie (3) jaar hebt verzorgd en opgevoed dan kan het kind door optie Nederlander worden als u zelf de Nederlandse nationaliteit heeft.
  Automatische verkrijging van het Nederlanderschap, verkrijging van rechtswege, vindt plaats indien uit de bepalingen van de Rijkswet op het Nederlanderschap volgt dat iemand de Nederlandse nationaliteit heeft. Voorbeelden hiervan zijn als een kind op het moment van geboorte een Nederlandse vader of een Nederlandse moeder heeft.</p>

  <p>Automatische verkrijging van het Nederlanderschap is soms ook mogelijk bij erkenning door een Nederlandse vader of moeder. Heeft u vragen over verkrijging van het Nederlanderschap door erkenning neem dan gerust contact met ons op. Wacht niet te lang want als het kind 7 jaar wordt gelden strengere eisen.</p>

