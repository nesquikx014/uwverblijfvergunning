  <h2>Arbeid</h2>
  <h2>
  	Verblijf voor werk (arbeid in loondienst)</h2>

  <p>
  Indien u of uw bedrijf een buitenlandse werknemer een contract aan wil bieden is het verstandig om u eerst door een jurist of advocaat voor te laten lichten over de te volgen procedure.</p>

  <p>Voor onderdanen van een land wat behoort tot de Europese Unie (EU), de Europees Economische Ruimte (EER) en Zwitserland geldt dat deze naar Nederland mogen komen om arbeid in loondienst te verrichten, zonder dat hiervoor een speciale toelatingsprocedure hoeft te worden gevolgd. Deze werknemers mogen gebruik maken van het recht op vrij verkeer.</p>

  <p>Anders ligt dit voor werknemers buiten de Europese Unie. Indien u de werknemer langer dan drie maanden bij u wil laten werken dient u bij de Immigratie- en Naturalisatiedienst een gecombineerde aanvraag in te dienen voor verblijf en arbeid (GVVA). In deze GVVA-procedure wordt de beoordeling gedaan door de IND, waarbij het wel zo is dat de IND zich laat adviseren door het UWV.</p>

