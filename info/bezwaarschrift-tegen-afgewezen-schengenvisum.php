  <h2>Bezwaarschrift tegen afgewezen Schengenvisum</h2>
  <p>
  Indien de Nederlandse Ambassade een aanvraag voor een Schengenvisum heeft afgewezen kunt u hiertegen in bezwaar gaan. Dit moet binnen vier weken.</p>

  <p>De Immigratie- en Naturalisatiedienst (de IND) is de organisatie die het bezwaarschrift zal beoordelen. De bezwaarschriftprocedure is een algehele heroverweging. Dit betekent dat alle feiten en omstandigheden, alsmede de ingediende bewijsstukken, wederom worden beoordeeld. Uiteraard staat het u vrij om nog meer bewijsstukken in te dienen. Sterker nog, dit raden wij ook aan.</p>

  <p>Een bezwaarschriftprocedure tegen een afgewezen Schengenvisum duurt ongeveer twaalf weken.</p>

  <p>Visumaanvragen worden vaak onterecht afgewezen. Dit komt door de volgende redenen:</p>

  <ul>
  <li>Het doel en de omstandigheden van het voorgenomen verblijf zijn onvoldoende aangetoond;</li>
  <li>Het voornemen om tijdig terug te keren naar het land van herkomst kon niet worden vastgesteld;</li>
  <li>De middelen van het voorgenomen verblijf zijn onvoldoende;</li>
  <li>De informatie die verstrekt is met betrekking tot het doel van het verblijf is niet betrouwbaar.</li>
  </ul>

  <h2>
  	Beroep</h2>

  <p>
  Als het bezwaar ongegrond is verklaard, dan krijgt u geen visum. Tegen een ongegrond bezwaar kan binnen 4 weken beroep worden aangetekend. Dit gebeurt bij de rechtbank. Wij kunnen namens u een beroepschrift opstellen.</p>

