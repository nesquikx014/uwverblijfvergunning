  <h2>Spoed visum</h2>
  <p>
  Een spoedvisum is een visum dat u met spoed nodig heeft om naar Nederland te komen. Bijvoorbeeld als u te maken heeft met een noodsituatie, zoals een ernstig ziek familielid in Nederland en u geen tijd heeft voor de normale procedure.</p>

  <p>Een spoedvisum is maximaal 90 dagen geldig.</p>

