<section class="article-section">
  <h2>Tips voor een sterk dossier</h2>
  <div class="article-callout article-callout--tip">
    <ul class="article-list">
      <li>Maak een zorglogboek waarin u wekelijks notities bijhoudt van dagelijkse taken.</li>
      <li>Vraag om schriftelijke verklaringen van school, kinderopvang of hulpverleners.</li>
      <li>Bewaar alle correspondentie met de andere ouder over de verdeling van zorg.</li>
      <li>Laat het kind (indien passend qua leeftijd) een brief schrijven over de band met u.</li>
    </ul>
  </div>
</section>

<section class="article-section">
  <h2>Wat als de IND uw aanvraag afwijst?</h2>
  <p>
    De IND kan oordelen dat de afhankelijkheidsrelatie onvoldoende is aangetoond. Wij analyseren de motivering en dienen een
    <strong>bezwaarschrift</strong> in met aanvullende stukken. In urgente situaties vragen we een voorlopige voorziening aan zodat u in Nederland
    kunt blijven tijdens de procedure.
  </p>
</section>

