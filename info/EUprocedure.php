<section class="article-section">
  <h2>Verblijf bij een minderjarig Nederlands kind (Chavez-Vilchez)</h2>
  <p>
    Het arrest <strong>Chavez-Vilchez</strong> van het Hof van Justitie (10 mei 2017) bevestigt dat een ouder zonder EU-nationaliteit een
    afgeleid verblijfsrecht kan krijgen wanneer een minderjarig Nederlands kind afhankelijk is van die ouder. Het uitgangspunt is het belang
    van het kind: een kind mag niet gedwongen worden de Europese Unie te verlaten omdat een ouder geen verblijfsrecht heeft.
  </p>
  <div class="article-callout">
    <strong>Belangrijk:</strong> Het arrest draait om de re&euml;le afhankelijkheidsrelatie tussen ouder en kind. Hoe centraler uw rol in de dagelijkse
    verzorging, hoe sterker uw aanspraak.
  </div>
</section>

<section class="article-section">
  <h2>Wanneer komt u in aanmerking?</h2>
  <p>De IND gaat na of het kind zonder u effectief kan blijven in Nederland. Daarbij wegen de volgende factoren zwaar mee:</p>
  <ul class="article-list">
    <li><strong>Dagelijkse zorg:</strong> u verricht feitelijke zorg- en opvoedtaken zoals ophalen van school, medische afspraken en huiswerkbegeleiding.</li>
    <li><strong>Emotionele betrokkenheid:</strong> er bestaat een nauwe band tussen u en uw kind, onderbouwd met verklaringen of fotoverslagen.</li>
    <li><strong>Financi&euml;le bijdrage:</strong> u draagt zichtbaar bij aan kosten voor voeding, kleding, huur of andere essenti&euml;le uitgaven.</li>
    <li><strong>Beschikbaarheid van de Nederlandse ouder:</strong> de andere ouder kan de zorg niet zelfstandig overnemen, bijvoorbeeld door werk, gezondheid of afstand.</li>
  </ul>
</section>

<section class="article-section">
  <h2>Documenten die overtuigen</h2>
  <p>De IND hanteert sinds 2018 hogere eisen. Met onderstaande documenten versterkt u uw aanvraag:</p>
  <div class="article-grid">
    <div>
      <h3>Verzorging &amp; opvoeding</h3>
      <ul class="article-list">
        <li>Schoolinschrijvingen en rapporten waarop u als contactpersoon vermeld staat</li>
        <li>Bewijs van medische afspraken (huisarts, tandarts) waar u bij aanwezig bent</li>
        <li>Kalenders of co-ouderschapsafspraken waarin uw aandeel zichtbaar is</li>
      </ul>
    </div>
    <div>
      <h3>Financi&euml;le bijdrage</h3>
      <ul class="article-list">
        <li>Bankafschriften waaruit vaste uitgaven blijken (boodschappen, huur, kinderopvang)</li>
        <li>Kwitanties of contracten op uw naam, bijvoorbeeld van sportclub of BSO</li>
        <li>Eventuele alimentatie-afspraken of betalingsbewijzen</li>
      </ul>
    </div>
    <div>
      <h3>Sociale context</h3>
      <ul class="article-list">
        <li>Verklaringen van leraren, hulpverleners of familieleden</li>
        <li>Foto- en dagboekmateriaal dat het dagelijks gezinsleven toont</li>
        <li>Overzichten van gezamenlijke activiteiten en vakanties</li>
      </ul>
    </div>
  </div>
</section>

