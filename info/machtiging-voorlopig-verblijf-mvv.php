  <h2>Machtiging voorlopig verblijf MVV</h2>
  <p>
  Een Machtiging tot voorlopig verblijf moet worden aangevraagd als u langer dan 3 maanden in Nederland wilt verblijven. Ook als u op termijn een vergunning wilt aanvragen moet u in veel gevallen eerste een MVV aanvragen.</p>

  <p>U kunt binnen 90 dagen een besluit verwachten van de IND. Soms kan dit wat langer duren. Wordt de mvv-aanvraag goedgekeurd, dan ontvangt uw buitenlandse partner of buitenlands familielid een mvv-sticker in het paspoort. Met de mvv-sticker kan uw partner of familielid naar Nederland afreizen. Na binnenkomst in Nederland kan de verblijfsvergunning bij het dichtstbijzijnde IND-loket worden opgehaald.</p>

