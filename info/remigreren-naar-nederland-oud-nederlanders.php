  <h2>Remigreren naar Nederland – Oud Nederlanders</h2>
  <h2>
  	Terugkeer en Terugkrijgen van de Nederlandse nationaliteit</h2>

  <p>
  Bent u oud Nederlander en heeft u de Nederlandse nationaliteit verloren in verband met het aannemen van een vreemde nationaliteit? Of bent u in het buitenland geboren met de Nederlandse nationaliteit en een vreemde nationaliteit en heeft u de Nederlandse nationaliteit verloren?</p>

  <p>Het kan zijn dat u in aanmerking komt om de Nederlandse nationaliteit te herkrijgen, maar het hangt er vanaf welk soort “Oud Nederlander” u bent. De Optie procedure is meestal de meest toegankelijke en snelste methode.</p>

  <p>U bent Oud Nederlander en bent in het Europese deel van Nederland geboren en getogen en u heeft de Nederlandse nationaliteit verloren nadat u Nederland had verlaten. Is deze situatie op u van toepassing?  Dan komt u in aanmerking voor het speciale visum “Wedertoelating”. Dit visum/verblijfsvergunning wordt alleen in het Europese deel van Nederland verstrekt. U moet wel aan alle voorwaarden voldoen.</p>

